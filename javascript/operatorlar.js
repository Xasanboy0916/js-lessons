//prefix increment (++x)
let x = 7;
let y = ++x;
console.log(x);
console.log(y);
//prefix decrement (--x)
let a = 12;
let b = a--;
console.log(a);
console.log(b);
//postfix increment (x++)
let m = 9;
let n = m++;
console.log(m);
console.log(n);
//postfix decrement (x--)
let p = 15;
let q = --p;
console.log(p);
console.log(q);

console.log(77 == "77"); //true
console.log(5 == 7); //false
